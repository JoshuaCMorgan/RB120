# all three of its arguments to the superclass `Animal`.  But `Animal`
# accepts only 2 arguments.
# You would need to give super two of the three arguments passed in.

# Also, `FlightlessBird#initialize` is unecessary since it subclasses `Animal`,
# which has an `#initialize` method of two arguments.

